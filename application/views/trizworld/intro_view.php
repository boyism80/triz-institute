					<p>자원이란 활용할 수 있는 모든 것을 가리킨다. 자원의 종류를 분류하면 다음과 같다.</p>
					<ul class="box-list box" style="width: 65%;">
						<li>물질(Substance) : 고체, 액체, 기체, 플라그마</li>
						<li>장(Field) : 물질 사이의 상호관계, 물질 사이에 작용하는 에너지(합)</li>
						<li>공간 : 시스템, 하위시스템, 상위시스템의 면, 빈 공간</li>
						<li>시간 : 어떤 공정이나 기능의 수행 전 시간, 수행 시의 시간, 수행 후의 시간</li>
						<li>정보 : 필요한 정보를 제공해주는 시스템에 존재하고 있는 물질 혹은 장</li>
