							즉 시스템은 그 기능을 수행하는 목적으로 사용하는 에너지, 싯간, 공간을 점점 줄이게 된다. 이상성에
							도달한 시슽엠은 시스템 자체는 사라지지만 그 기능은 유지하는 상태이다. 이것은 시스템이 상위 시스템의 일부가
							되었거나 시스템이 완전히 자신의 기능을 사우이 시스템의 요소드렝게 전달했기 때문이다. 따라서 시스템 자체는 더
							이상 필요가 없어지게 되어 사라지게 되는 것이다. 이상적인 시스템이란 무게가 전혀 나가지 않으며, 공간을
							차지하지도 않고 에너지를 소비하지도 않으면서 시스템 작동(기능)은 수행하고 있다. 창의적으로 문제를 해결하고자
