		<p>알츠슐러는 모순을 포함하고 있는 창의적 문제의 해결에 있어 발명원리가 중요한 도구이나, 이것만으로는 부족하다는 것을 깨닫게 된다. 언제나 모순이 바로 규명되고 그에 합당한 원리를 적용하는 것이 쉬운 일이 아니다. 무엇보다 모순을 규명하기 위한 문제의 분석이 필요했다. 또한 발명 원리를 적용하기 위해서는 강력한 상상력이 요구되었다. 많은 경우 모순 해결은 몇 가지 원리를 동시에 적용해야만 가능했다.</p>
		<br />
		<p>그렇다면 어떻게 할 것인가? 강력한 문제해결의 길은 있는가?</p>
		<br />
